public class DobroTriplo {
   public static void main(String[] args) {
      
      int numero = 100;
      int x5 = numero * 5; // calcula o quintuplo de numero.
      int x10 = numero * 10; //calcula o décuplo de numero.
   
      // mostra o numero e o resultado do dobro e do triplo.
      System.out.printf("O quintuplo de %d é: %d%n", numero, x5);
      System.out.printf("O décuplo de %d é: %d%n", numero, x10);


   }
}